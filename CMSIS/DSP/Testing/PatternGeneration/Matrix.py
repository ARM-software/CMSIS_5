def cartesian(*somelists):
   r=[]
   for element in itertools.product(*somelists):
       r.append(element)
   return(r)

def swaprow(a,k,j):
    tmp = np.copy(a[j,:])
    a[j,:] = np.copy(a[k,:])
    a[k,:] = tmp 
    return(a)

def swapcol(a,k,j):
    tmp = np.copy(a[:,j])
    a[:,j] = np.copy(a[:,k])
    a[:,k] = tmp 
    return(a)

def ldlt(src):
    # Algorithm 4.2.2 in Golub and Van Loan
    ma = np.copy(src)
    fullRank = True


    piv=np.zeros(len(ma),dtype=int)

    n = len(ma)

    for k in range(0,n):
        piv[k] = k
    
    for k in range(0,n):
        d=np.diagonal(ma)
        j = np.argmax(d[k:]) + k
        piv[k] = j
        
        ma = swaprow(ma,k,j)
        ma = swapcol(ma,k,j)
        
        alpha = ma[k,k]
        v = np.copy(ma[k+1:,k])

        if abs(alpha) < 1.0e-18:
            fullRank = False
            break
    
        ma[k+1:,k] = v / alpha
    
        v = v.reshape((n-k-1,1))
    
        ma[k+1:,k+1:] = ma[k+1:,k+1:] - np.matmul(v , np.transpose(v)) / alpha
    
    
    if not fullRank:
       ma[:,k:] = 0.0
       diags=(np.array(range(0,k),dtype=int),np.array(range(0,k),dtype=int))
    else:
       diags=(np.array(range(0,k+1),dtype=int),np.array(range(0,k+1),dtype=int))

    ll=np.tril(ma)
       
    ll[diags] = 1.0
    d=np.diag(np.diagonal(ma))

    return(ll,d,piv)

# Validate the Python implementation of LDLT
def valid(src,ll,d,piv):
    n = len(src)
    p=np.identity(n)
    for k in range(0,n):
        p = swaprow(p,k,piv[k])
    
    a = np.matmul(p,np.matmul(src,np.transpose(p)))
    t = np.matmul(ll,np.matmul(d,np.transpose(ll)))
    r = a - t
    r[abs(r)<1e-10]=0.0
    return(np.all(r == 0.0))

